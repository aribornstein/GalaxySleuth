
public class Accusation {

	Card[] accusation;
	Card[] solution;
	Boolean result;
	
	public Accusation(Card[] accusation, Card[] solution, Boolean result) {
		super();
		this.accusation = accusation;
		this.solution = solution;
		this.result = result;
	}
	
	public Boolean getResult(){
		return result;
	}
}
